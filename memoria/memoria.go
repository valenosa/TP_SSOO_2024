package main

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////IMPORTS//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

import (
	"fmt"
	"log"
	"net/http"
	"strconv"

	"github.com/sisoputnfrba/tp-golang/utils/APIs/kernel-memoria/proceso"
	"github.com/sisoputnfrba/tp-golang/utils/config"
)

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////VARIABLES && STRUCTS//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////MAIN///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

func main() {

	// Configura el logger
	config.Logger("Memoria.log")

	log.Printf("Soy un logeano")

	// Se establece el handler que se utilizar√° para las diversas situaciones recibidas por el server

	http.HandleFunc("PUT /process", proceso.HandlerIniciar)
	http.HandleFunc("DELETE /process/{pid}", proceso.HandlerFinalizar)
	http.HandleFunc("GET /process/{pid}", proceso.HandlerEstado)
	http.HandleFunc("GET /process", proceso.HandlerListar)

	// Extrae info de config.json
	var configJson config.Memoria

	config.Iniciar("config.json", &configJson)

	// declaro puerto
	port := ":" + strconv.Itoa(configJson.Port)

	// Listen and serve con info del config.json
	err := http.ListenAndServe(port, nil)
	if err != nil {
		fmt.Println("Error al esuchar en el puerto " + port)
	}
}
